import { Document } from "mongoose";

/**
 * Интерфейс для модели Rarity в базе данных
 * 
 * Rarity определяет уровень редкости предметов в системе.
 * Используется для визуального оформления предметов и управления вероятностью их выпадения.
 */
export interface IRarity extends Document {
  // Название редкости
  // Ограничено набором допустимых значений для обеспечения консистентности данных
  name: "Common" | "Uncommon" | "Rare" | "Epic" | "Legendary" | "Gold";
  
  // HEX-код цвета для отображения редкости на фронтенде
  // Например, "#9d9d9d" для Common (серый), "#ffd700" для Gold (золотой)
  color: string; // HEX
  
  // Базовый шанс выпадения предметов этой редкости (в процентах)
  // Используется как ориентир, но конкретный шанс для каждого предмета
  // в каждом кейсе задается индивидуально в модели CaseItem
  chance: number; // %
}

