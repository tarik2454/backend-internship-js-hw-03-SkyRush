import { Document, Types } from "mongoose";

/**
 * Интерфейс для модели Item в базе данных
 *
 * Item представляет предмет, который может выпасть из кейса.
 * Каждый предмет имеет название, редкость, изображение и стоимость.
 */
export interface IItem extends Document {
  // Название предмета (например, "AK-47 Redline", "Knife Butterfly")
  name: string;

  // ID редкости предмета (ссылка на коллекцию "rarities")
  // Редкость определяет визуальное оформление и базовую редкость предмета
  rarityId: Types.ObjectId;

  // URL изображения предмета (для отображения на фронтенде)
  imageUrl: string;

  // Стоимость предмета (в игровой валюте или реальных деньгах)
  // Используется для расчета статистики пользователя (totalWon)
  value: number;

  // Дата и время создания предмета (автоматически устанавливается при создании)
  createdAt: Date;
}
